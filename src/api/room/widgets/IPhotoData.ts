export interface IPhotoData
{
    /**
     * creator username
     */
    n?: string;

    /**
     * creator user id
     */
    s?: number;

    /**
     * photo unique id
     */
    u?: number;

    /**
     * creation timestamp
     */
    t?: number;

    /**
     * photo caption
     */
    m?: string;

    /**
     * photo image url
     */
    w?: string;

    /**
     * owner id
     */
    oi?: number;

    /**
     * owner name
     */
    o?: string;
}